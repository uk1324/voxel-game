#include <glad/glad.h>

void Primitives::init()
{
	if (s_isInitialized == false)
	{
        cubeTrianglesVao = std::make_unique<VertexArray>();
        cubeTrianglesVao->bind();
        cubeTrianglesVbo = std::make_unique<VertexBuffer>(cubeTriangleVertices, sizeof(cubeTriangleVertices));
        cubeTrianglesVbo->bind();
        glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, sizeof(float) * 3, reinterpret_cast<void*>(0));
        glEnableVertexAttribArray(0);
	}
}

bool Primitives::s_isInitialized = false;